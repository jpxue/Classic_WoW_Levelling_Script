				local distBetween = CalculateDistance(p1[1],p1[2],p1[3],p2[1],p2[2],p2[3])
				
				if distBetween > 50 then
					DbgPrint('Reversing Path!!!')
					local i, j = 1, #waypoints
					while i < j do
						waypoints[i], waypoints[j] = waypoints[j], waypoints[i]
						i = i + 1
						j = j - 1
					end
				else
					DbgPrint('ReLooping Path!!!')
				end
				
				pathIdx = 1
			else
				DbgPrint('Cannot ReLoop as Start is not in LOS!')
				pathIdx = 1
			end
		else
			DbgPrint('Finished Pathing!!!')
			Exit('PathEndCheck')
		end
	end	
end

