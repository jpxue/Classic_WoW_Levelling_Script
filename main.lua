							Attack(attackObj)
						else
							local castingID = UnitCastID("player")
							if castingID ~= 0 and inCombat == false then --Mage problem
								strDbg = "PREVENTING_MAGE_CAST_INTERR"
								--
							else
								if bTargetIsFar and bMountWhileGrinding and IsIndoors() == false and IsMounted() == false and HasMount() then
									if MountUp() then
										strDbg = "MOUNTING_TO_NEXT_TARGET"
										DbgPrint('Target is far away... Mounting!')
									end
								else
									strDbg = "MOVEMENT_LAST"
									PulseMovement()
									PathEndCheck()
								end
							end
						end
					else
						strDbg = "LOOTING_SKINNING"
						SetIDXToClosest(true, 10)
					end

				end
				
				
			end
		else
			if table.getn(avoidNPCs) > 0 then
				FindAttackableUnit() --still need to avoid dangerous NPCs
			end
			strDbg = "VITALS_FAILED"
			--DbgPrint("Vitals x(")
		end
			
